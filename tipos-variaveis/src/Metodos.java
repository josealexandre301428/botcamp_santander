public class Metodos {
    public static void main(String[] args) {//Metodo main
        System.out.println("Hello World");
    }
    //deve ser nomeado como verbo
    //Seguir camelCase
    // se o metodo nao retorna nada, deve ser void
    //se o metodo retorna algo, deve ser do tipo do retorno
    //metodos podem receber parametros
    //parametros sao valores passados para o metodo

    public int somar(int a, int b){//Metodo para somar, recebe parametros do tipo int e retorna um int
        return a+b;
    }
}
